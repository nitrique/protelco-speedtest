    /**
     * Lancement du téléchargement
     * @param evt Default Event
     */
    private void bStartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bStartActionPerformed
        this.bStart.setEnabled(false);
        this.areaLog.setText(null);
        this.totalSpeed = 0.0;
        this.idBcl = 0;
        
        SwingWorker<?,?> worker = new SwingWorker() {

            @Override
            protected Object doInBackground() throws Exception {
                threadDL dl = new threadDL(MainFrame.this, 0);
                dl.run();
                return null;
            }
        };
        SwingWorker<?,?> worker1 = new SwingWorker() {

            @Override
            protected Object doInBackground() throws Exception {
                threadDL dl = new threadDL(MainFrame.this, 1);
                dl.run();
                return null;
            }
        };
        SwingWorker<?,?> worker2 = new SwingWorker() {

            @Override
            protected Object doInBackground() throws Exception {
                threadDL dl = new threadDL(MainFrame.this, 2);
                dl.run();
                return null;
            }
        };
        SwingWorker<?,?> worker3 = new SwingWorker() {

            @Override
            protected Object doInBackground() throws Exception {
                threadDL dl = new threadDL(MainFrame.this, 3);
                dl.run();
                return null;
            }
        };
        
        worker.execute();
        worker1.execute();
        worker2.execute();
        worker3.execute();
        
    }//GEN-LAST:event_bStartActionPerformed


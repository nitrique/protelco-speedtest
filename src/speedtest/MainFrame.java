    /**
     * Calcul de la vitesse totale
     * @param speed Vitesse
     */
    public void addTotalSpeed(Double speed)
    {
        totalSpeed += speed;
        idBcl++;
        
        if (idBcl == 4)
        {
            Double acces = ((totalSpeed*8)/1000);
            
            String formated = new DecimalFormat("##.###").format(acces);
            addLog("--> Vitesse cumulée : " + totalSpeed + " Kio/s, Soit à un accès d'environ " + formated + " Mégas ATM");
            this.bStart.setEnabled(true);
        }
    }
    
